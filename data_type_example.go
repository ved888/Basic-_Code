package main

import (
	"fmt"
)

func main() {

	//example of integerng data type
	var a int = 5

	fmt.Printf("data type is %T and value is %v \n", a, a)

	//example of boolean data type
	var b bool

	fmt.Printf("data type is %T and value is %v \n", b, b)

	//example of string data type
	var s string = "hello everyone"

	fmt.Printf("data type is %T and value is %v \n", s, s)
}
