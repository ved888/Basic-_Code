package main

import "fmt"

func main() {

	var a int = 6

	// if else statement to check variable a has greater than 5 value or less than 5
	if a <= 5 {
		fmt.Println("value of a is less than or eqal to 5")
	} else {
		fmt.Println("value of a is greater than 5")
	}

	//if statement
	if a > 5 {
		fmt.Println("value of a is greater than 5")
	}

}
